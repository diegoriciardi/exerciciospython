"""
Exemplo 4.27 Uso do tipo range
"""

print("list(range(10): \n", list(range(10)))
print("list(range(5, 15): \n", list(range(5, 15)))
print("list(range(-3, 4): \n", list(range(-3, 4)))
print("list(range(5, 15, 3): \n", list(range(5, 15, 3)))
print("list(range(10, 0, -1): \n", list(range(10, 0, -1)))
print("list(range(6, -1, 2): \n", list(range(6, -1, 2)))
print("list(range(10, 3, 1): \n", list(range(10, 3, 1)))